#loop in list

fruits = ["apple", "banana", "cherry"] #list of fruits  
for fruit in fruits: #iterate through each fruit in the list
    print(fruit) #print the current fruit
    print(fruit.upper()) #print the current fruit in uppercase
    print(fruit.lower()) #print the current fruit in lowercase
    print(fruit.title()) #print the current fruit in title case
    print(fruit.capitalize()) #print the current fruit with first letter capitalized
    print("-----") #print a separator for better readability    
