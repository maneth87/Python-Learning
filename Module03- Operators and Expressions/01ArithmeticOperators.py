#Arithmatic Operators: +,-,*,/
num1 = 10
num2 = 3
sum = num1 + num2
print("The Sum ",num1,"+",num2," is:",sum)

subtract = num1 - num2
print("The Subtraction",num1,"-",num2," is:",subtract)

multiply = num1 * num2
print("The Multiplication",num1,"*",num2," is:",multiply)

divide = num1 / num2
print("The Division",num1,"/",num2," is:",divide)

#floor division, not rounding
floor_div = num1 // num2    
print("The Floor Division",num1,"//",num2," is:",floor_div) 

#modulus operator, result is the remainder
modulus = num1 % num2   
print("The Modulus",num1,"%",num2," is:",modulus)


#exponentiation operator
exponentiation = num1 ** num2       
print("The Exponentiation",num1,"**",num2," is:",exponentiation)