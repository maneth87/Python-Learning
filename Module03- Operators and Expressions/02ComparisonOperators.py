'''
==, !=, >, <, >=, <= are comparison operators in Python.
'''

#equal to (== )
number =10==10
print("10==10 is ",number) #True

number =10==5
print("10==5 is",number) #False

#not equal to (!=)
number =10!=5
print("10!=5 is ",number) #True

number =10!=10
print("10!=10 is ",number) #False

#greater than (>)
number =10>5    
print("10>5 is",number) #True
number =5>10    
print("5>10 is",number) #False

#less than (<)
number =5<10    
print("5<10 is",number) #True
number =10<5    
print("10<5 is",number) #False

#greater than or equal to (>=)
number =10>=5   
print("10>5 is",number) #True
number =5>=10   
print("5>=10 is",number) #False

#less than or equal to (<=)
number =5<=10   
print("5<=10 is",number) #True
number =10<=5
print("10<=5 is",number) #False


